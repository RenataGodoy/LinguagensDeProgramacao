# Tupla com nome de alunos e suas respectivas notas
alunos = ("Renata", 10., "Gabriel homosapiens", 10.0, "Flavio julho", 9.2, "Rafinha lutador", 10.0, "Guido piupiu doidao", 8.0)

# Exibir apenas os nomes dos alunos
nomes = alunos[::2]
